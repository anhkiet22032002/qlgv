SELECT KQ.MALOP, KQ.MAHV, KQ.HOTEN, KQ.SOLUONG
FROM  ( SELECT HV.MALOP, HV.MAHV, HV.HO + ' ' + HV.TEN AS HOTEN, COUNT(DIEM) AS SOLUONG, 
				RANK() OVER(PARTITION BY HV.MALOP ORDER BY COUNT(*) DESC) AS score_rank
		FROM dbo.HOCVIEN AS HV JOIN dbo.KETQUATHI AS KQ ON KQ.MAHV = HV.MAHV
		WHERE DIEM >= 9
		GROUP BY MALOP, HV.MAHV, HV.HO, HV.TEN) AS KQ
WHERE kq.score_rank = 1
