--35. ** Tìm học viên (mã học viên, họ tên) có điểm thi cao nhất trong từng môn (lấy điểm ở lần thi sau
--cùng).
SELECT KQ.MAMH, KQ.MAHV, KQ.HOTEN
FROM ( SELECT KQT.MAMH, HV.MAHV, HV.HO + ' ' + HV.TEN AS HOTEN, RANK() OVER(PARTITION BY KQT.MAMH ORDER BY MAX(DIEM) DESC) AS max_rank
		FROM dbo.HOCVIEN AS HV JOIN dbo.KETQUATHI AS KQT ON KQT.MAHV = HV.MAHV
		WHERE KQT.LANTHI = (SELECT MAX(LANTHI) FROM dbo.KETQUATHI WHERE MAHV = HV.MAHV GROUP BY MAHV)
		GROUP BY MAMH, HV.MAHV, HV.HO, HV.TEN) AS KQ
WHERE KQ.max_rank = 1