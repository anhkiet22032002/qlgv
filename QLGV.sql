--2. Mã học viên là một chuỗi 5 ký tự, 3 ký tự đầu là mã lớp, 2 ký tự cuối cùng là số thứ tự học viên
--trong lớp. VD: “K1101”
CREATE TRIGGER UTR_HOCVIEN
ON HOCVIEN
FOR INSERT, UPDATE 
AS
BEGIN
	DECLARE @SISO TINYINT, @MAHV CHAR(5), @MALOP CHAR(3)

	SELECT @MAHV = MAHV, @MALOP = MALOP FROM INSERTED
	SELECT @SISO = SISO FROM LOP WHERE LOP.MALOP = @MALOP

	IF LEFT(@MAHV,3) <> @MALOP
	BEGIN
		PRINT('3 ky tu dau cua MAHV phai la MALOP')
		ROLLBACK TRAN
	END
	
	ELSE IF CAST(RIGHT(@MAHV, 2) AS INT) NOT BETWEEN 1 AND @SISO
	BEGIN
		PRINT('2 ky tu cuoi cua MAHV phai la so thu tu hoc vien trong lop')
		ROLLBACK TRAN
	END
END
GO

